-- 6. Scenario pricing marts
-- ============================================================

CREATE TABLE mart.mart_pricing_scenarios AS
SELECT *
FROM read_csv_auto(
    'outputs/tables/pricing_scenario_summary.csv',
    HEADER = TRUE
);

-- One-row executive pricing summary for Power BI KPI cards
CREATE TABLE mart.mart_pricing_summary AS
WITH scenario_values AS (
    SELECT
        MAX(CASE WHEN scenario_name = 'Base Case' THEN expected_loss END) AS base_scenario_premium,
        MAX(CASE WHEN scenario_name = 'Stress Scenario' THEN expected_loss END) AS stress_scenario_premium,
        MAX(CASE WHEN scenario_name = 'Stress Scenario' THEN expected_loss_pct_change_from_base END) AS stress_premium_change_pct
    FROM mart.mart_pricing_scenarios
),
selected_model AS (
    SELECT
        distribution AS selected_severity_model
    FROM mart.mart_candidate_models
    WHERE best_fit = '✓'
)
SELECT
    selected_model.selected_severity_model,
    scenario_values.base_scenario_premium,
    scenario_values.stress_scenario_premium,
    scenario_values.stress_premium_change_pct
FROM scenario_values
CROSS JOIN selected_model;

CREATE TABLE mart.mart_pricing_scenario_percentiles AS
SELECT *
FROM read_csv_auto(
    'outputs/tables/pricing_scenario_percentiles.csv',
    HEADER = TRUE
);

