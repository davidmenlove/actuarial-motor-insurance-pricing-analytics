DROP TABLE IF EXISTS mart.mart_frequency_by_region;
DROP TABLE IF EXISTS mart.mart_frequency_by_vehicle_brand;
DROP TABLE IF EXISTS mart.mart_frequency_by_vehicle_fuel;
DROP TABLE IF EXISTS mart.mart_bonus_malus;
